import { compareTwoSnapshots } from "../compareTwoSnapshots.js"
import { isAdded, isModified, isDeleted } from "./helper.js"
import { renderImpactTable } from "./renderImpactTable.js"

export const HEADER = `<!-- Generated by @jsenv/file-size-impact -->`

const GITHUB_MAX_COMMENT_LENGTH = 65536
// let's be large and keep 5000 over 65536 (GITHUB_MAX_COMMENT_LENGTH)
// for header, warnings and footer (footer is added by reportFileSizeImpact)
// so if body length exceed 60000 (60ko) we truncate it, otherwise
// github would not let us post the comment
const COMMENT_BODY_MAX_LENGTH = GITHUB_MAX_COMMENT_LENGTH - 5000

export const formatComment = ({
  cacheImpact,
  trackingConfig,
  transformations,
  pullRequestBase,
  pullRequestHead,
  baseSnapshot,
  afterMergeSnapshot,

  formatGroupSummary,
  formatHiddenImpactSummary,
  formatFileRelativeUrl,
  maxRowsPerTable,
  fileRelativeUrlMaxLength,
  formatFileCell,
  formatFileSizeImpactCell,
  formatSize,
}) => {
  const warnings = []
  const snapshotComparison = compareTwoSnapshots(baseSnapshot, afterMergeSnapshot)
  const groups = Object.keys(snapshotComparison)
  const groupCount = groups.length

  if (groupCount === 0) {
    warnings.push(
      `**Warning:** Nothing is tracked. It happens when tracking config is an empty object.`,
    )
  }

  // call formatFileSize only on numbers 'error' must be returned untouched
  const formatSizeOriginal = formatSize
  formatSize = (value, ...rest) => {
    if (typeof value === "number") return formatSizeOriginal(value, ...rest)
    return value
  }

  let body = renderCommentBody({
    cacheImpact,
    trackingConfig,
    transformations,
    pullRequestBase,
    pullRequestHead,
    snapshotComparison,

    formatGroupSummary,
    formatHiddenImpactSummary,
    fileRelativeUrlMaxLength,
    maxRowsPerTable,
    formatFileRelativeUrl,
    formatFileCell,
    formatFileSizeImpactCell,
  })

  const bodyLength = Buffer.byteLength(body)
  if (bodyLength > COMMENT_BODY_MAX_LENGTH) {
    body = `${body.slice(0, COMMENT_BODY_MAX_LENGTH)}â€¦`
    warnings.push(
      `**Warning:** The comment body was truncated to fit GitHub limit on comment length.
Your trackingConfig is maybe tracking too much files ?
As the body is truncated the message might be hard to read.
For the record the full comment length was ${formatSize(bodyLength, { unit: true })}.`,
    )
  }

  const head = `${HEADER}
<h4 id="file-size-impact">File size impact</h4>`
  const warning = renderWarnings(warnings)

  const parts = [warning, body].filter((string) => string.length > 0)

  return `${head}

${parts.join(`

`)}`
}

const renderWarnings = (warnings) => {
  if (warnings.length === 0) {
    return ""
  }

  return `---

${warnings.join(`

`)}

---`
}

const renderCommentBody = ({
  cacheImpact,
  trackingConfig,
  transformations,
  pullRequestBase,
  pullRequestHead,
  snapshotComparison,

  formatGroupSummary,
  formatHiddenImpactSummary,
  fileRelativeUrlMaxLength,
  maxRowsPerTable,
  formatFileRelativeUrl,
  formatFileCell,
  formatFileSizeImpactCell,
}) => {
  const overallImpactInfo = {}

  const groupMessages = Object.keys(snapshotComparison).map((groupName) => {
    const groupComparison = snapshotComparison[groupName]
    const groupLength = Object.keys(groupComparison).length
    if (groupLength === 0) {
      return renderEmptyGroup({
        groupName,
        groupConfig: trackingConfig[groupName],
        formatGroupSummary,
      })
    }

    const fileByFileImpact = {}
    const fileByFileImpactHidden = {}

    const addImpact = (fileRelativeUrl, { event, sizeImpactMap, base, afterMerge }) => {
      const meta = event === "deleted" ? base.meta : afterMerge.meta
      const impact = {
        event,
        sizeImpactMap,
        base,
        afterMerge,
        ...(cacheImpact
          ? { participatesToCacheImpact: event === "modified" || event === "added" }
          : {}),
      }

      if (!cacheImpact && !hasSizeImpact(sizeImpactMap)) {
        return
      }

      const data = metaToData(meta, fileRelativeUrl, impact)
      if (data.showSizeImpact) {
        if (!overallImpactInfo.hasOwnProperty(fileRelativeUrl)) {
          overallImpactInfo[fileRelativeUrl] = groupName
        }

        fileByFileImpact[fileRelativeUrl] = { ...impact, ...data }
      } else {
        fileByFileImpactHidden[fileRelativeUrl] = { ...impact, ...data }
      }
    }

    Object.keys(groupComparison).forEach((fileRelativeUrl) => {
      const { base, afterMerge } = groupComparison[fileRelativeUrl]

      if (isAdded({ base, afterMerge })) {
        const event = "added"
        const sizeImpactMap = {}
        const sizeMapAfterMerge = afterMerge.sizeMap
        Object.keys(sizeMapAfterMerge).forEach((sizeName) => {
          sizeImpactMap[sizeName] = sizeMapAfterMerge[sizeName]
        })
        addImpact(fileRelativeUrl, {
          event,
          sizeImpactMap,
          base,
          afterMerge,
        })
        return
      }

      if (isDeleted({ base, afterMerge })) {
        const event = "deleted"
        const sizeImpactMap = {}
        const sizeMapOnBase = base.sizeMap
        Object.keys(sizeMapOnBase).forEach((sizeName) => {
          sizeImpactMap[sizeName] = -sizeMapOnBase[sizeName]
        })
        addImpact(fileRelativeUrl, {
          event,
          sizeImpactMap,
          base,
          afterMerge,
        })
        return
      }

      if (isModified({ base, afterMerge })) {
        const event = "modified"
        const sizeImpactMap = {}
        const sizeMapOnBase = base.sizeMap
        const sizeMapAfterMerge = afterMerge.sizeMap
        Object.keys(sizeMapAfterMerge).forEach((sizeName) => {
          sizeImpactMap[sizeName] = sizeMapAfterMerge[sizeName] - sizeMapOnBase[sizeName]
        })
        addImpact(fileRelativeUrl, {
          event,
          sizeImpactMap,
          base,
          afterMerge,
        })
      }
    })

    const groupImpactCount = Object.keys(fileByFileImpact).length
    const groupHiddenImpactCount = Object.keys(fileByFileImpactHidden).length
    if (groupImpactCount === 0 && groupHiddenImpactCount === 0) {
      return `<details>
  <summary>${formatGroupSummary({ groupName, groupImpactCount, groupLength })}</summary>
  <p>No impact on files in ${groupName} group.</p>
</details>`
    }

    const elements = [
      ...(groupImpactCount > 0
        ? [
            renderImpactTable(fileByFileImpact, {
              transformations,
              fileRelativeUrlMaxLength,
              maxRowsPerTable,
              formatFileRelativeUrl,
              formatFileCell,
              formatFileSizeImpactCell,
            }),
          ]
        : []),
      ...(groupHiddenImpactCount > 0
        ? [
            `<details>
  <summary>${formatHiddenImpactSummary({ groupName, groupHiddenImpactCount })}</summary>
  ${renderImpactTable(fileByFileImpactHidden, {
    transformations,
    fileRelativeUrlMaxLength,
    maxRowsPerTable,
    formatFileRelativeUrl,
    formatFileCell,
    formatFileSizeImpactCell,
  })}
  </details>`,
          ]
        : []),
    ]

    return `<details>
  <summary>${formatGroupSummary({ groupName, groupImpactCount, groupLength })}</summary>
  ${elements.join(`
  `)}
</details>`
  })

  const mergeImpact = formulateMergeImpact({ pullRequestHead, pullRequestBase, overallImpactInfo })
  if (groupMessages.length === 0) {
    return mergeImpact
  }

  return `${mergeImpact}
${groupMessages.join(`

`)}`
}

const formulateMergeImpact = ({ pullRequestBase, pullRequestHead, overallImpactInfo }) => {
  const overallImpact = formulateOverallImpact(overallImpactInfo)
  return `<p>Merging ${pullRequestHead} into ${pullRequestBase} will ${overallImpact}.</p>`
}

const formulateOverallImpact = (overallImpactInfo) => {
  let impactedFileCount = 0
  let impactedGroupCount = 0
  const impactedGroups = []
  Object.keys(overallImpactInfo).forEach((relativeUrl) => {
    const groupName = overallImpactInfo[relativeUrl]
    if (groupName) {
      impactedFileCount++
      if (!impactedGroups.includes(groupName)) {
        impactedGroups.push(groupName)
        impactedGroupCount++
      }
    }
  })

  if (impactedFileCount === 0) {
    return `not impact files in any group`
  }
  return `impact ${formulateFileQuantity(impactedFileCount)} in ${formulateGroupQuantity(
    impactedGroupCount,
  )}`
}

const formulateFileQuantity = (count) => {
  return count === 1 ? `1 file` : `${count} files`
}

const formulateGroupQuantity = (count) => {
  return count === 1 ? `1 group` : `${count} groups`
}

const hasSizeImpact = (sizeImpactMap) => {
  return Object.keys(sizeImpactMap).some((sizeName) => sizeImpactMap[sizeName] !== 0)
}

const renderEmptyGroup = ({ groupName, groupConfig, formatGroupSummary }) => {
  return `<details>
  <summary>${formatGroupSummary({
    groupName,
    groupLength: 0,
    groupImpactCount: 0,
  })}</summary>
  <p>No file in ${groupName} group (see config below).</p>

\`\`\`json
${JSON.stringify(groupConfig, null, "  ")}
\`\`\`

</details>`
}

const metaToData = (meta, ...args) => {
  if (typeof meta === "boolean") {
    return {
      showSizeImpact: true,
    }
  }

  if (typeof meta === "object") {
    const showSizeImpact = showSizeImpactGetter(meta, ...args)
    const { formatFileRelativeUrl } = meta
    return {
      showSizeImpact,
      formatFileRelativeUrl,
    }
  }

  console.warn(`meta must be a boolean or a function, received ${meta}`)
  return {
    showSizeImpact: Boolean(meta),
  }
}

const showSizeImpactGetter = (
  meta,
  fileRelativeUrl,
  { event, sizeImpactMap, base, afterMerge },
) => {
  const { showSizeImpact = true } = meta
  if (typeof showSizeImpact === "boolean") {
    return showSizeImpact
  }

  if (typeof showSizeImpact === "function") {
    return showSizeImpact({
      fileRelativeUrl,
      event,
      sizeImpactMap,
      sizeMapOnBase: base ? base.sizeMap : null,
      sizeMapAfterMerge: afterMerge.sizeMap,
    })
  }

  console.warn(`${showSizeImpact} must be a boolean or a function, received ${showSizeImpact}`)
  return true
}
